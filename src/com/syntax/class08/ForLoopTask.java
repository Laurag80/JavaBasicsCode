package com.syntax.class08;

public class ForLoopTask {

	public static void main(String[] args) {
		
		System.out.println("-------------");
		for (int i=1; i<=100; i++) {
			System.out.print(i+" ");
		}
		System.out.println("-----------");
		for (int i=100; i>=1; i--) {
			System.out.print(i+" ");
		}
		System.out.println("-----------");
		for (int i=20; i>=1; i-=2) {
			System.out.print(i+" ");

	}

}
}
